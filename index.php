<script>
    const serviceDivMain = document.querySelector('.service_div_main');
    const serviceCardMain = document.querySelector('.service_card_main');
    const serviceCardTitle = document.querySelector('.service_card_title');

    // Add scroll event listener
    window.addEventListener('scroll', () => {
        // Get the current scroll position
        const scrollAmountY = window.scrollY;

        if (((serviceCardTitle.offsetTop - 100) / serviceDivMain.clientHeight) * 100 > 74) {
            serviceCardMain.classList.add("fade_out_div");
        } else {
            serviceCardMain.classList.remove("fade_out_div");
        }
        let winwidth = window.innerWidth;
        // Move `serviceCardMain` horizontally based on scrollY
        if (winwidth < 500) {
            serviceCardMain.style.transform = `translateX(-${(scrollAmountY-200)/12}%)`;
        } else {
            serviceCardMain.style.transform = `translateX(-${(scrollAmountY-400)/12}%)`;
        }
    });
</script>